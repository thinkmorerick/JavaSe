package cn.util.Scanner;

public class Operation06 {

	public static void main(String[] args) {
		/**
		 * ==:基本数据类型在做比较的时候,是比较的 值！ 
		 * !=:不等于
		 */

		int a = 5;
		int b = 5;
		System.out.println("a == b:" + (a == b));
		System.out.println("a != b:" + (a != b));
		System.out.println("a > b:" + (a > b));
		System.out.println("a < b:" + (a < b));
		System.out.println("a >= b:" + (a >= b));
		System.out.println("a <= b:" + (a <= b));
	}

}
